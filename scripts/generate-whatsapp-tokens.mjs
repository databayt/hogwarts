const BANNER = `/* AUTOGENERATED by scripts/generate-whatsapp-tokens.mjs — DO NOT EDIT BY HAND.\n   Source of truth: ~/.claude/memory/whatsapp_tokens.json\n   Regenerate with: pnpm tsx scripts/generate-whatsapp-tokens.mjs */`

function toKebab(key) {
  return key.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase()
}

function cssVarName(key) {
  return `--wa-${toKebab(key)}`
}

function toSnake(key) {
  return key.replace(/([a-z])([A-Z])/g, "$1_$2").toLowerCase()
}

function toCamel(key) {
  return key
}

function iconToSwift(name) {
  return name
    .replace(/-/g, "_")
    .replace(/^ic_wa_/, "")
    .replace(/^(\d)/, "_$1")
}

function iconToKotlin(name) {
  return name.replace(/-/g, "_").toUpperCase()
}

function parseColor(c) {
  if (c.startsWith("#")) {
    const hex = c.slice(1)
    const n = parseInt(
      hex.length === 3
        ? hex
            .split("")
            .map((x) => x + x)
            .join("")
        : hex,
      16
    )
    return { r: (n >> 16) & 0xff, g: (n >> 8) & 0xff, b: n & 0xff, a: 1 }
  }
  const m = c.match(
    /rgba?\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)(?:\s*,\s*([\d.]+))?\s*\)/
  )
  if (m)
    return { r: +m[1], g: +m[2], b: +m[3], a: m[4] !== undefined ? +m[4] : 1 }
  return null
}

function cssColor(c) {
  return c
}

function swiftColor(c) {
  const p = parseColor(c)
  if (!p) return `Color.clear /* unparsed: ${c} */`
  return `Color(red: ${(p.r / 255).toFixed(3)}, green: ${(p.g / 255).toFixed(3)}, blue: ${(p.b / 255).toFixed(3)}, opacity: ${p.a})`
}

function kotlinColor(c) {
  const p = parseColor(c)
  if (!p) return `Color.Unspecified /* unparsed: ${c} */`
  const alpha = Math.round(p.a * 255)
    .toString(16)
    .padStart(2, "0")
  const r = p.r.toString(16).padStart(2, "0")
  const g = p.g.toString(16).padStart(2, "0")
  const b = p.b.toString(16).padStart(2, "0")
  const hex = `${alpha}${r}${g}${b}`.toUpperCase()
  return `Color(0x${hex})`
}

async function writeIfChanged(path, content, dryRun) {
  if (dryRun) {
    console.log(`  would write: ${path} (${content.length} bytes)`)
    return "would-write"
  }
  await mkdir(dirname(path), { recursive: true })
  const prev = existsSync(path) ? await readFile(path, "utf8") : ""
  if (prev === content) return "unchanged"
  await writeFile(path, content)
  return prev === "" ? "created" : "updated"
}

async function appendGlobalsCss(tokens, dryRun) {
  const globalsPath = join(HOGWARTS_ROOT, "src/app/globals.css")
  if (!existsSync(globalsPath)) {
    console.warn(`  (skip) globals.css not found at ${globalsPath}`)
    return
  }
  const existing = await readFile(globalsPath, "utf8")

  const lightBlock = Object.entries(tokens.colors.light)
    .filter(([k]) => !k.startsWith("_"))
    .map(([k, v]) => `  ${cssVarName(k)}: ${cssColor(v)};`)
    .join("\n")

  const darkBlock = Object.entries(tokens.colors.dark)
    .filter(([k]) => !k.startsWith("_"))
    .map(([k, v]) => `  ${cssVarName(k)}: ${cssColor(v)};`)
    .join("\n")

  const marker = "/* WA TOKENS START */"
  const endMarker = "/* WA TOKENS END */"
  const block = `${marker}\n:root {\n${lightBlock}\n}\n.dark {\n${darkBlock}\n}\n${endMarker}`

  const esc = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&")
  let next
  if (existing.includes(marker)) {
    next = existing.replace(
      new RegExp(`${esc(marker)}[\\s\\S]*?${esc(endMarker)}`),
      () => block
    )
  } else {
    next = existing.trimEnd() + "\n\n" + block + "\n"
  }

  const result = await writeIfChanged(globalsPath, next, dryRun)
  console.log(`  globals.css: ${result}`)
}

async function writeTsTokens(tokens, dryRun) {
  const path = join(
    HOGWARTS_ROOT,
    "src/components/school-dashboard/messaging/mobile/wa-tokens.ts"
  )

  const cdnBase = tokens.icons._cdnBase
  const iconList = tokens.icons._list

  const content = `${BANNER.replace("/*", "//")
    .replace("*/", "")
    .split("\n")
    .map((l) => "// " + l.replace(/^\s*\*?\s?/, "").trim())
    .join("\n")}

export const WA_CDN_BASE = "${cdnBase}" as const;

export const WA_ICONS = {
${iconList.map((n) => `  "${n}": \`\${WA_CDN_BASE}/${n}.svg\``).join(",\n")}
} as const;

export type WaIconName = keyof typeof WA_ICONS;

export function waIcon(name: WaIconName): string {
  return WA_ICONS[name];
}

export const WA_DIMENSIONS = ${JSON.stringify(tokens.dimensions, null, 2)} as const;

export const WA_TYPOGRAPHY = ${JSON.stringify(tokens.typography, null, 2)} as const;
`

  const result = await writeIfChanged(path, content, dryRun)
  console.log(`  wa-tokens.ts: ${result}`)
}

async function writeKotlin(tokens, dryRun) {
  const colorsPath = join(
    KOTLIN_ROOT,
    "core/designsystem/src/main/java/org/hogwarts/android/core/designsystem/theme/whatsapp-colors.kt"
  )
  const iconsPath = join(
    KOTLIN_ROOT,
    "core/designsystem/src/main/java/org/hogwarts/android/core/designsystem/theme/wa-icons.kt"
  )

  if (!existsSync(dirname(colorsPath))) {
    console.warn(`  (skip kotlin) ${dirname(colorsPath)} not found`)
    return
  }

  const lightFields = Object.entries(tokens.colors.light).filter(
    ([k]) => !k.startsWith("_")
  )
  const darkFields = Object.entries(tokens.colors.dark).filter(
    ([k]) => !k.startsWith("_")
  )

  const colorsKt = `// AUTOGENERATED — DO NOT EDIT. Source: ~/.claude/memory/whatsapp_tokens.json
package org.hogwarts.android.core.designsystem.theme

import androidx.compose.runtime.Immutable
import androidx.compose.runtime.compositionLocalOf
import androidx.compose.ui.graphics.Color

@Immutable
data class WhatsAppColors(
${lightFields.map(([k]) => `    val ${k}: Color,`).join("\n")}
) {
    companion object {
        val light = WhatsAppColors(
${lightFields.map(([k, v]) => `            ${k} = ${kotlinColor(v)},`).join("\n")}
        )
        val dark = WhatsAppColors(
${darkFields.map(([k, v]) => `            ${k} = ${kotlinColor(v)},`).join("\n")}
        )
    }
}

val LocalWhatsAppColors = compositionLocalOf { WhatsAppColors.light }
`

  const iconList = tokens.icons._list
  const iconsKt = `// AUTOGENERATED — DO NOT EDIT. Source: ~/.claude/memory/whatsapp_tokens.json
package org.hogwarts.android.core.designsystem.theme

object WaIcons {
    const val CDN_BASE = "${tokens.icons._cdnBase}"

${iconList.map((n) => `    const val ${iconToKotlin(n)} = "${tokens.icons._cdnBase}/${n}.svg"`).join("\n")}
}
`

  const r1 = await writeIfChanged(colorsPath, colorsKt, dryRun)
  const r2 = await writeIfChanged(iconsPath, iconsKt, dryRun)
  console.log(`  whatsapp-colors.kt: ${r1}`)
  console.log(`  wa-icons.kt: ${r2}`)
}

async function writeSwift(tokens, dryRun) {
  const colorsPath = join(
    SWIFT_ROOT,
    "hogwarts/shared/ui/design-system/whatsapp-colors.swift"
  )
  const iconsPath = join(
    SWIFT_ROOT,
    "hogwarts/shared/ui/design-system/wa-icons.swift"
  )

  if (!existsSync(dirname(colorsPath))) {
    console.warn(`  (skip swift) ${dirname(colorsPath)} not found`)
    return
  }

  const lightFields = Object.entries(tokens.colors.light).filter(
    ([k]) => !k.startsWith("_")
  )
  const darkFields = Object.entries(tokens.colors.dark).filter(
    ([k]) => !k.startsWith("_")
  )

  const colorsSwift = `// AUTOGENERATED — DO NOT EDIT. Source: ~/.claude/memory/whatsapp_tokens.json
import SwiftUI

struct WhatsAppColors {
${lightFields.map(([k]) => `    let ${k}: Color`).join("\n")}

    static let light = WhatsAppColors(
${lightFields.map(([k, v], i, arr) => `        ${k}: ${swiftColor(v)}${i < arr.length - 1 ? "," : ""}`).join("\n")}
    )

    static let dark = WhatsAppColors(
${darkFields.map(([k, v], i, arr) => `        ${k}: ${swiftColor(v)}${i < arr.length - 1 ? "," : ""}`).join("\n")}
    )
}

private struct WhatsAppColorsKey: EnvironmentKey {
    static let defaultValue: WhatsAppColors = .light
}

extension EnvironmentValues {
    var whatsAppColors: WhatsAppColors {
        get { self[WhatsAppColorsKey.self] }
        set { self[WhatsAppColorsKey.self] = newValue }
    }
}
`

  const iconList = tokens.icons._list
  const iconsSwift = `// AUTOGENERATED — DO NOT EDIT. Source: ~/.claude/memory/whatsapp_tokens.json
import Foundation

enum WaIcons {
    static let cdnBase = "${tokens.icons._cdnBase}"

${iconList.map((n) => `    static let ${iconToSwift(n)} = URL(string: "${tokens.icons._cdnBase}/${n}.svg")!`).join("\n")}
}
`

  const r1 = await writeIfChanged(colorsPath, colorsSwift, dryRun)
  const r2 = await writeIfChanged(iconsPath, iconsSwift, dryRun)
  console.log(`  whatsapp-colors.swift: ${r1}`)
  console.log(`  wa-icons.swift: ${r2}`)
}

async function main() {
  const dryRun = process.argv.includes("--dry-run")
  const tokens = JSON.parse(await readFile(TOKENS_PATH, "utf8"))

  console.log(`Source: ${TOKENS_PATH}`)
  console.log(`Mode:   ${dryRun ? "DRY-RUN" : "WRITE"}\n`)

  console.log("Web:")
  await appendGlobalsCss(tokens, dryRun)
  await writeTsTokens(tokens, dryRun)

  console.log("\nAndroid:")
  await writeKotlin(tokens, dryRun)

  console.log("\niOS:")
  await writeSwift(tokens, dryRun)

  console.log("\nDone.")
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
