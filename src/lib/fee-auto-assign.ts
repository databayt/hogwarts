import { db } from "@/lib/db"

/**
 * Founder contract — every student-create / grade-update path that knows the
 * student's grade MUST end with `ensureStudentFeeAssignments`. The guarantee:
 *
 * 1. Synchronous + awaited (callers `await` the result; no fire-and-forget).
 * 2. Idempotent — re-running for the same `(student, structure, year)` tuple
 *    is a no-op via `@@unique([studentId, feeStructureId, academicYear])`.
 * 3. Never overwrites admin-set fields. Existing `discounts` / `scholarshipId`
 *    / `totalDiscount` / `customAmount` / `finalAmount` are left alone — only
 *    NEW assignments are inserted.
 * 4. Best-effort invoice sync + `fee_due` notification fire after the
 *    transaction commits; failures here log but never roll back the
 *    assignments themselves.
 */
export interface EnsureFeeAssignmentsInput {
  schoolId: string
  studentId: string
  /** When null, the helper short-circuits to `skipped: 1` — caller is
   *  expected to re-invoke once a grade is set. */
  academicGradeId: string | null
  /** Defaults to the school's current `SchoolYear.yearName`, or the calendar
   *  year-pair fallback. */
  academicYear?: string
  /** When false, suppress the `fee_due` notification — useful for bulk
   *  imports where notifications are dispatched separately. */
  notify?: boolean
}

export interface EnsureFeeAssignmentsResult {
  created: number
  existing: number
  /** 1 when the call short-circuited because no grade was supplied. */
  skipped: number
  /** Combined IDs of newly-created + pre-existing assignments. Useful for
   *  invoice fan-out by the caller. */
  assignmentIds: string[]
}

export async function ensureStudentFeeAssignments(
  input: EnsureFeeAssignmentsInput
): Promise<EnsureFeeAssignmentsResult> {
  const { schoolId, studentId, academicGradeId, notify = true } = input

  // No grade yet → nothing to assign. Caller (wizard, future grade-edit) is
  // expected to re-invoke once `Student.academicGradeId` is set.
  if (!academicGradeId) {
    return { created: 0, existing: 0, skipped: 1, assignmentIds: [] }
  }

  const academicYear =
    input.academicYear ?? (await resolveCurrentAcademicYear(schoolId))

  // Grade-specific structures attach via Class, so we resolve those classes
  // first.
  const gradeClasses = await db.class.findMany({
    where: { schoolId, gradeId: academicGradeId },
    select: { id: true },
  })
  const gradeClassIds = gradeClasses.map((c) => c.id)

  // Three sources of structures match a student-grade pair:
  // 1. Admin-created school-wide (no class link, not auto-generated)
  // 2. Class-linked structures whose classId belongs to this grade
  // 3. Auto-generated per-grade structures (sourceSignals.gradeId match)
  const feeStructures = await db.feeStructure.findMany({
    where: {
      schoolId,
      academicYear,
      isActive: true,
      OR: [
        { classId: null, isAutoGenerated: false },
        ...(gradeClassIds.length > 0
          ? [{ classId: { in: gradeClassIds } }]
          : []),
        {
          isAutoGenerated: true,
          sourceSignals: {
            path: ["gradeId"],
            equals: academicGradeId,
          },
        },
      ],
    },
    select: { id: true, totalAmount: true },
  })

  if (feeStructures.length === 0) {
    return { created: 0, existing: 0, skipped: 0, assignmentIds: [] }
  }

  // Diff existing vs needed so we know exactly how many were *newly* created.
  // Counting via upsert would conflate new+existing and break the founder
  // contract's notification rule (only notify on new).
  const structureIds = feeStructures.map((s) => s.id)
  const existing = await db.feeAssignment.findMany({
    where: { studentId, academicYear, feeStructureId: { in: structureIds } },
    select: { id: true, feeStructureId: true },
  })
  const existingStructureIds = new Set(existing.map((a) => a.feeStructureId))
  const missing = feeStructures.filter((s) => !existingStructureIds.has(s.id))

  if (missing.length === 0) {
    return {
      created: 0,
      existing: existing.length,
      skipped: 0,
      assignmentIds: existing.map((a) => a.id),
    }
  }

  // Atomic create of the missing assignments. A partial failure must not
  // leave half-created rows — `db.$transaction` rolls back on throw.
  const created = await db.$transaction(async (tx) => {
    const createdRows: { id: string }[] = []
    for (const structure of missing) {
      try {
        const row = await tx.feeAssignment.create({
          data: {
            schoolId,
            studentId,
            feeStructureId: structure.id,
            academicYear,
            finalAmount: Number(structure.totalAmount),
            totalDiscount: 0,
            status: "PENDING",
          },
          select: { id: true },
        })
        createdRows.push(row)
      } catch (err: unknown) {
        // Race fallback: another concurrent caller inserted between our
        // diff query and this create. The unique constraint catches it
        // (Prisma error P2002); we treat as pre-existing and continue.
        if (
          err &&
          typeof err === "object" &&
          "code" in err &&
          (err as { code?: string }).code === "P2002"
        ) {
          continue
        }
        throw err
      }
    }
    return createdRows
  })

  const allAssignmentIds = [
    ...existing.map((a) => a.id),
    ...created.map((c) => c.id),
  ]

  // Side-effects after commit. These intentionally live OUTSIDE the
  // transaction — a transient failure here (e.g., notification provider
  // timeout) must never roll back the assignment.
  if (created.length > 0) {
    try {
      const { ensureInvoicesForAssignment } =
        await import("@/lib/fee-invoice-sync")
      await Promise.all(
        created.map((c) =>
          ensureInvoicesForAssignment(schoolId, c.id).catch((err) =>
            console.warn(
              `[ensureStudentFeeAssignments] Invoice sync failed for assignment=${c.id}:`,
              err
            )
          )
        )
      )
    } catch (err) {
      console.warn(
        "[ensureStudentFeeAssignments] Invoice sync batch failed:",
        err
      )
    }
  }

  if (notify && created.length > 0) {
    try {
      await notifyStudentFeesAssigned(schoolId, studentId, created.length)
    } catch (err) {
      console.warn("[ensureStudentFeeAssignments] Notification failed:", err)
    }
  }

  if (created.length > 0) {
    console.log(
      `[ensureStudentFeeAssignments] Created ${created.length} new fee assignment(s) for student ${studentId} (${existing.length} pre-existing)`
    )
  }

  return {
    created: created.length,
    existing: existing.length,
    skipped: 0,
    assignmentIds: allAssignmentIds,
  }
}

/**
 * @deprecated Prefer `ensureStudentFeeAssignments`. Thin wrapper kept so the
 * rewiring sweep can be done one caller at a time without breakage.
 */
export async function autoAssignFeesForStudent(
  schoolId: string,
  studentId: string,
  academicGradeId: string
): Promise<{ assignedCount: number }> {
  const result = await ensureStudentFeeAssignments({
    schoolId,
    studentId,
    academicGradeId,
  })
  // Old contract counted EVERY matching structure; preserve that for callers
  // still on the deprecated entrypoint.
  return { assignedCount: result.created + result.existing }
}

async function resolveCurrentAcademicYear(schoolId: string): Promise<string> {
  const currentYear = await db.schoolYear.findFirst({
    where: { schoolId },
    orderBy: { startDate: "desc" },
    select: { yearName: true },
  })
  return (
    currentYear?.yearName ??
    `${new Date().getFullYear()}-${new Date().getFullYear() + 1}`
  )
}

async function notifyStudentFeesAssigned(
  schoolId: string,
  studentId: string,
  assignmentCount: number
): Promise<void> {
  const student = await db.student.findFirst({
    where: { id: studentId, schoolId },
    select: {
      firstName: true,
      userId: true,
      studentGuardians: {
        select: {
          guardian: {
            select: { userId: true },
          },
        },
      },
    },
  })
  if (!student) return

  const [school, { dispatchNotification }] = await Promise.all([
    db.school.findUnique({
      where: { id: schoolId },
      select: { preferredLanguage: true, name: true },
    }),
    import("@/lib/dispatch-notification"),
  ])

  const lang = school?.preferredLanguage ?? "ar"
  const schoolName = school?.name ?? ""

  // Titles + bodies — kept inline (small surface) but stored against the
  // school's preferredLanguage. Runtime translation handles the rest.
  const title =
    lang === "ar" ? `تم تحديد الرسوم الدراسية` : `Tuition fees assigned`
  const body =
    lang === "ar"
      ? `تمت إضافة ${assignmentCount} رسم${assignmentCount === 1 ? "" : "وم"} لطالبك. يمكنك المراجعة والسداد عبر صفحة الرسوم.`
      : `${assignmentCount} fee${assignmentCount === 1 ? "" : "s"} assigned to your student at ${schoolName}. Review and pay from the My Fees page.`

  const recipients = new Set<string>()
  if (student.userId) recipients.add(student.userId)
  for (const sg of student.studentGuardians) {
    if (sg.guardian?.userId) recipients.add(sg.guardian.userId)
  }

  await Promise.all(
    Array.from(recipients).map((userId) =>
      dispatchNotification({
        schoolId,
        userId,
        type: "fee_due",
        title,
        body,
        lang,
        priority: "normal",
        channels: ["in_app", "email", "whatsapp"],
        metadata: { studentId, assignmentCount },
      }).catch((err) =>
        console.warn(
          `[notifyStudentFeesAssigned] Failed for user=${userId}:`,
          err
        )
      )
    )
  )
}
