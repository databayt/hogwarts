// Helper to get preset by ID
export function getPresetById(id: string): ThemePreset | undefined {
  return themePresets.find((preset) => preset.id === id)
}

// Helper to get all preset IDs
export function getPresetIds(): string[] {
  return themePresets.map((preset) => preset.id)
}

// Helper to filter presets by tag
export function getPresetsByTag(tag: string): ThemePreset[] {
  return themePresets.filter((preset) => preset.tags?.includes(tag))
}
